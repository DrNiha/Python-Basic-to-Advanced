marks = float(input("Please enter your percentage:\n"))

if(marks>90 and marks<100):
    print("Woo-hoo!! Your grade is EXCELLENT!!")
elif(marks>80 and marks<90):
    print("Yay!! Your grade is A!!")
elif(marks>70 and marks<80):
    print("Yay!! Your grade is B!!")
elif(marks>60 and marks<70):
    print("Yay!! Your grade is C!!")
elif(marks>50 and marks<60):
    print("Yay!! Your grade is D!!")
else:
    print("Your grade is F!!")
